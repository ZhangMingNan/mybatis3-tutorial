package com.ly.zmn48644.tutorial.model;

public class Info {
    private String time;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    @Override
    public String toString() {
        return "Info{" +
                "time='" + time + '\'' +
                '}';
    }
}
